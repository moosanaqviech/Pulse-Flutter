                          ),
                          
                          const SizedBox(height: 24),
                          
                          Consumer<AuthService>(
                            builder: (context, authService, _) {
                              return CustomButton(
                                text: 'Send Reset Link',
                                onPressed: authService.isLoading ? null : _resetPassword,
                                isLoading: authService.isLoading,
                              );
                            },
